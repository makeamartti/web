function getLang() {
  return localStorage.getItem('mm-lang') || 'fi';
}

function setLang(lang) {
  localStorage.setItem('mm-lang', lang);
  applyTranslations(lang);
  document.querySelectorAll('.lang-switch button').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
  document.documentElement.lang = lang;
}

function applyTranslations(lang) {
  const t = translations[lang];
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (t[key]) {
      el.textContent = t[key];
    }
  });
}

document.addEventListener('DOMContentLoaded', () => {
  const lang = getLang();
  setLang(lang);

  document.querySelectorAll('.lang-switch button').forEach(btn => {
    btn.addEventListener('click', () => setLang(btn.dataset.lang));
  });

  // Mobile menu
  const toggle = document.querySelector('.mobile-menu-toggle');
  const navLinks = document.querySelector('.nav-links');
  if (toggle && navLinks) {
    toggle.addEventListener('click', () => {
      toggle.classList.toggle('open');
      navLinks.classList.toggle('open');
    });
    navLinks.querySelectorAll('a').forEach(link => {
      link.addEventListener('click', () => {
        toggle.classList.remove('open');
        navLinks.classList.remove('open');
      });
    });
  }

  // Scroll reveal
  const reveals = document.querySelectorAll('.reveal');
  if (reveals.length) {
    const observer = new IntersectionObserver((entries) => {
      entries.forEach(entry => {
        if (entry.isIntersecting) {
          entry.target.classList.add('visible');
        }
      });
    }, { threshold: 0.1 });
    reveals.forEach(el => observer.observe(el));
  }

  // Custom audio player
  const audioEl = document.querySelector('.lyric-player audio');
  if (audioEl) {
    audioEl.removeAttribute('controls');
    const wrap = audioEl.closest('.lyric-player');
    const player = document.createElement('div');
    player.className = 'custom-player';
    player.innerHTML = `
      <button class="player-btn" aria-label="Play" data-action="play">
        <svg viewBox="0 0 24 24" class="icon-play"><polygon points="6,3 20,12 6,21"/></svg>
        <svg viewBox="0 0 24 24" class="icon-pause" style="display:none"><rect x="5" y="3" width="4" height="18"/><rect x="15" y="3" width="4" height="18"/></svg>
      </button>
      <div class="player-progress-wrap">
        <span class="player-time player-time--current">0:00</span>
        <div class="player-track"><div class="player-track-fill"></div></div>
        <span class="player-time player-time--duration">0:00</span>
      </div>
    `;
    wrap.appendChild(player);

    const btn = player.querySelector('[data-action="play"]');
    const iconPlay = player.querySelector('.icon-play');
    const iconPause = player.querySelector('.icon-pause');
    const fill = player.querySelector('.player-track-fill');
    const track = player.querySelector('.player-track');
    const timeCur = player.querySelector('.player-time--current');
    const timeDur = player.querySelector('.player-time--duration');

    function fmt(s) {
      const m = Math.floor(s / 60);
      const sec = Math.floor(s % 60);
      return m + ':' + (sec < 10 ? '0' : '') + sec;
    }

    btn.addEventListener('click', () => {
      if (audioEl.paused) { audioEl.play(); } else { audioEl.pause(); }
    });

    audioEl.addEventListener('play', () => {
      iconPlay.style.display = 'none';
      iconPause.style.display = 'block';
    });
    audioEl.addEventListener('pause', () => {
      iconPlay.style.display = 'block';
      iconPause.style.display = 'none';
    });
    audioEl.addEventListener('loadedmetadata', () => {
      timeDur.textContent = fmt(audioEl.duration);
    });
    audioEl.addEventListener('timeupdate', () => {
      if (audioEl.duration) {
        fill.style.width = (audioEl.currentTime / audioEl.duration * 100) + '%';
        timeCur.textContent = fmt(audioEl.currentTime);
      }
    });
    audioEl.addEventListener('ended', () => {
      iconPlay.style.display = 'block';
      iconPause.style.display = 'none';
      fill.style.width = '0%';
      timeCur.textContent = '0:00';
    });

    track.addEventListener('click', (e) => {
      const rect = track.getBoundingClientRect();
      const ratio = (e.clientX - rect.left) / rect.width;
      if (audioEl.duration) {
        audioEl.currentTime = ratio * audioEl.duration;
      }
    });
  }

  // Lightbox
  const lightbox = document.getElementById('lightbox');
  if (lightbox) {
    const lightboxImg = lightbox.querySelector('img');
    document.querySelectorAll('.gallery-item img').forEach(img => {
      img.addEventListener('click', () => {
        lightboxImg.src = img.src;
        lightbox.classList.add('active');
      });
    });
    lightbox.addEventListener('click', () => {
      lightbox.classList.remove('active');
    });
  }
});
